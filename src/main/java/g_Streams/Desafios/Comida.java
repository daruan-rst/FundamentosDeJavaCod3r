package g_Streams.Desafios;

public class Comida {

    final String nome;

    final double peso;
    final boolean contemGluten;
    final double calorias;

    public Comida(String nome, double peso, boolean contemGluten, double calorias) {
        this.nome = nome;
        this.peso = peso;
        this.contemGluten = contemGluten;
        this.calorias = calorias;
    }

}
